set1 = {1, 2, 3}
set2 = {3, 4, 5}

# Using union() method
print(f"The Union Of {set1} & {set2} Is ::: {set1.union(set2)}")  

# Using | operator
print(f"The Union Of {set1} & {set2} Is ::: {set1 | set2}")  

print("\n")

# Using intersection() method
print(f"The Intersection Of {set1} & {set2} Is ::: {set1.intersection(set2)}")  

# Using & operator
print(f"The Intersection Of {set1} & {set2} Is ::: {set1 & set2}")  